class Pivotnode  #< ActiveRecord::Base

  # ========================================================
  # 全般
  # ========================================================
  # --------------------------------------------------------
  # 抽象クラスであることを、フレームワークに対して宣言
  # --------------------------------------------------------
  # self.abstract_class = true
  
  # --------------------------------------------------------
  # 初期化
  # --------------------------------------------------------
  def initialize
    @axises = Hash.new()
  end
  
  # --------------------------------------------------------
  # 軸情報の丸ごと取得
  # --------------------------------------------------------
  def getAxises
    return @axises
  end
  
  # --------------------------------------------------------
  # 軸情報の丸ごとセット
  # --------------------------------------------------------
  def setAxises(axises)
    @axises     = axises
  end
  
  # --------------------------------------------------------
  # 軸情報のクローンを丸ごと取得
  # --------------------------------------------------------
  def getDupedAxises
    return self.getAxises.dup
  end
  
  # --------------------------------------------------------
  # 軸の追加	
  # --------------------------------------------------------
  def addAxis(axisname, axisvalue)
    @axises.store(axisname, axisvalue)
  end

  # --------------------------------------------------------
  # 子ノードの数（デフォルトは１）	
  # --------------------------------------------------------
  def numberOfLeaf(counter)
    return 1
  end
  
  # --------------------------------------------------------
  # 行の末端かどうか（ツリー表示のための機能）
  # --------------------------------------------------------
  def endOfRow?
    true
  end
  
  # --------------------------------------------------------
  # 内容の出力	
  # --------------------------------------------------------
  def to_s
    s = String.new()
    @axises.each do | key, value |
      s = s + '  ' + key + '->' + value
    end
    #s = s  + ?\n
    return s
  end
  
end
