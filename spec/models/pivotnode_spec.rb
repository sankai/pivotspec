class PivotNode  < ActiveRecord::Base

  # ========================================================
  # 全般
  # ========================================================
  # --------------------------------------------------------
  # 抽象クラスであることを、フレームワークに対して宣言
  # --------------------------------------------------------
  self.abstract_class = true
  
  # --------------------------------------------------------
  # 初期化
  # --------------------------------------------------------
  def after_initialize
    @axises = Hash.new()
  end

  # --------------------------------------------------------
  # 軸の追加	
  # --------------------------------------------------------
  def addAxis(axisname, axisvalue)
    @axises.store(axisname, axisvalue)
  end
end
